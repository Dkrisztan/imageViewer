function backward() {
    
    if (nr > 0) {
        nr = nr - 1;
        document.getElementById("forward").disabled = false;
        var s = images[nr];
        document.getElementById("kep").src = `images/${s}`;
    }
    if (nr == 1) document.getElementById("backward").disabled = true;
    else document.getElementById("backward").disabled = false;
}

var myVar;
var megy = false;

function indit() {
    if (!megy) {
        myVar = setInterval(myTimer, 500);
        megy = true;
        document.getElementById("media").src = "play.png";
        document.getElementById("zene").play();
        document.getElementById("zene").volume = 0.01;
    }
    else {
        megall();
        document.getElementById("media").src = "pause.png";
        document.getElementById("zene").pause();
    }
}

function megall() {
    if (megy) {
        clearInterval(myVar);
        megy = false;
    }
}

function myTimer() {
    if (nr < kepsz) {
        forward();
    } else nr = 0;
}

